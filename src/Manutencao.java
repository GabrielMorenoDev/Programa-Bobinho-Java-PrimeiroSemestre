    public void atualizarTabela(){
        try{
            Connection c = DBCConnection.getConnection();
            PreparedStatement ps = c.prepareStatement("select * from cachorro_clientes");
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            DefaultTableModel dtm = (DefaultTableModel) clientesTable.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                Vector v = new Vector();
                for(int i = 1; i<rsmd.getColumnCount(); i++){
                    v.add(rs.getString("nome"));
                    v.add(rs.getString("senha"));
                    v.add(rs.getString("signo"));
                    v.add(rs.getString("deusa"));
                    v.add(rs.getBoolean("admin"));
                }
                dtm.addRow(v);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
