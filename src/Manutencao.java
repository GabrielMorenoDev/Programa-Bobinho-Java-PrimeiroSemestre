    private void add(){
        try{
            Connection c = DBCConnection.getConnection();
            PreparedStatement ps = c.prepareStatement("insert into cachorro_clientes(nome, senha,signo,deusa,admin) values (?,?,?,?,?)");
            ps.setString(1, nomeTextField.getText());
            ps.setString(2, senhaTextField.getText());
            ps.setString(3, signoTextField.getText());
            ps.setString(4, deusaTextField.getText());
            ps.setBoolean(5, adminCheckBox.isSelected());
            ps.execute();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
