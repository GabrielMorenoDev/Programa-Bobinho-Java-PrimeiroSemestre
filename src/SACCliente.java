    private void darFeedbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_darFeedbackActionPerformed
        while(feedTextArea.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Não é possível mandar um feedback vazio... \n O não sei se comento pensa em comentar...");
        }
        try{
            Connection c = DBCConnection.getConnection();
            PreparedStatement pst = c.prepareStatement("insert into cachorro_feedback(feedback, usuario) values (?,?)");
            pst.setString(2, getUser());
            pst.setString(1, feedTextArea.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Feedback registrado!");
        }catch(Exception e){
         e.printStackTrace();
        }
        
        this.dispose();
    }//GEN-LAST:event_darFeedbackActionPerformed

