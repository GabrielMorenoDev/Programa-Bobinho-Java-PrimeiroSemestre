    public void atualizarTabelaFeedbacks(){
                try{
        Connection con = DBCConnection.getConnection();
        PreparedStatement pst = con.prepareStatement("select * from cachorro_feedback where usuario = ?");
        pst.setString(1, usuario);
        ResultSet rs = pst.executeQuery();
        ResultSetMetaData rsmd = rs.getMetaData();
        int n = rsmd.getColumnCount();
        DefaultTableModel dtm = (DefaultTableModel) feedTable.getModel();
        dtm.setRowCount(0);
        while(rs.next()){
            Vector v = new Vector();
            for(int i = 1; i<=n;i++ ){
                v.add(rs.getString("feedback"));
                v.add(rs.getString("resposta"));
            }
            dtm.addRow(v);
        }
        
        } catch(Exception e){
            e.printStackTrace();
        }
    }
    
