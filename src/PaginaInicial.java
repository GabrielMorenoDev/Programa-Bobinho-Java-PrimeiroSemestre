    private void atualizarCarrinhoDeCompras(){
        try{
        DefaultTableModel model = (DefaultTableModel)carrinhoTable.getModel();
        Connection c = DBCConnection.getConnection();
        PreparedStatement pst = c.prepareStatement("select * from cachorro_carrinho where usuario = ?");
        pst.setString(1, usuario);
        ResultSet rs = pst.executeQuery();
        ResultSetMetaData rsmd = rs.getMetaData();
        model.setRowCount(0);
        int n = rsmd.getColumnCount();
        while(rs.next()){
        Vector v = new Vector();
        for(int j = 1; j<=n; j++){
            v.add(rs.getString("nome"));
            v.add(rs.getInt("quantLGTV"));
            v.add(rs.getInt("quantOiCadela"));
            v.add(rs.getInt("quantMedo"));
            v.add(rs.getInt("quantSMT"));
            v.add(rs.getInt("quantNsei"));
            v.add(rs.getInt("quantPior"));
        }
        model.addRow(v);
        }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
        
