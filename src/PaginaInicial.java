    private void compLGTVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_compLGTVActionPerformed
        int n = Integer.parseInt(quantLGTV.getText());
        int totalComprados;
        if(n>0){
            totalComprados = Integer.parseInt(JOptionPane.showInputDialog("Quantos LGTV voce deseja comprar?"));
            while(totalComprados>n){
                JOptionPane.showMessageDialog(null, "Digite um valor valido... Nos nao temos tantos assim...");
                totalComprados = Integer.parseInt(JOptionPane.showInputDialog("Quantos LGTV voce deseja comprar?"));
            }while(totalComprados<=0){
                JOptionPane.showMessageDialog(null, "Nos nao temos cachorros negativos... e vc n pode comprar 0 cachorros agora que clicou em comprar");
                totalComprados = Integer.parseInt(JOptionPane.showInputDialog("Quantos LGTV voce deseja comprar?"));
            }if(totalComprados == n){
                    JOptionPane.showMessageDialog(null, "QUE ISSO PARCEIRO VOCE COMPROU TODOS LGTV???? eita...");
            }else{
                JOptionPane.showMessageDialog(null, "Voce comprou um LGTV!!! esse olhar dele diz mt sobre voce...");
            }
            try{
                Connection c = DBCConnection.getConnection();
                PreparedStatement pst = c.prepareStatement("insert into cachorro_compras(nome, quantLGTV, quantOiCadela, quantMedo, quantSMT, quantNSei, quantPiorqe) values (?,?,0,0,0,0,0)");
                pst.setString(1, usuario);
                pst.setInt(2, totalComprados);
                pst.execute();
            }catch(Exception e){
                e.printStackTrace();
            }
            n = n - totalComprados;
            quantLGTV.setText(Integer.toString(n));
        }else{
            JOptionPane.showMessageDialog(null, "Estamos sem lgtv no estoque... o mundo foi dominado?");
        }
    }//GEN-LAST:event_compLGTVActionPerformed
    
