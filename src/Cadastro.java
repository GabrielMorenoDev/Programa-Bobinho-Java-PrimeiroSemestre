    private void cadastrar(){
        try{
            Connection c = DBCConnection.getConnection();
            PreparedStatement pstCheckDuplicateQuery = c.prepareStatement("select * from cachorro_clientes where nome = ?");
            pstCheckDuplicateQuery.setString(1, nomeTextField.getText());
            ResultSet rs = pstCheckDuplicateQuery.executeQuery();
            if(!rs.next()){
            PreparedStatement pst = c.prepareStatement("insert into cachorro_clientes(nome, senha, signo, deusa, admin) values (?,?,?,?,0)");
             pst.setString(1, nomeTextField.getText());
             pst.setString(2, senhaTextField.getText());
             pst.setString(3, signoTextField.getText());
             pst.setString(4, deusaTextField.getText());
             pst.execute();
             JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!");
                 if(nomeTextField.getText().equals("Beatriz")){
                     JOptionPane.showMessageDialog(null, "Beatriz Ã© o nome da Deusa da Beleza!! Belo nome para cadastro");
                 }Login l = new Login();
            l.setVisible(true);
            this.dispose();}else{
                JOptionPane.showMessageDialog(null, "Nome ja cadastrado escolha outro!!!");
                cadastrar();
            }
 
        }catch(Exception e){
        e.printStackTrace();
        }
    }
